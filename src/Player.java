/**
 * Represents a player.
 *
 * @author Jason Wong
 * @author Emmy Truong
 */
public class Player {
    /** name of Player */
    private String name;

    /**
     * Sets name of player
     *
     * @param name String of name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets name of player
     *
     * @return String of name
     */
    public String getName() {
        return name;
    }
}
